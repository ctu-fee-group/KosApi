using System.Collections.Generic;
using System.Threading;
using System.Threading.Tasks;
using Kos.Atom;
using Kos.Data;

namespace Kos.Abstractions
{
    /// <summary>
    /// Represents mapping to programmes kos api.
    /// </summary>
    public interface IKosProgrammesApi
    {
        /// <summary>
        /// Gets paged programmes filtered using RSQL query.
        /// </summary>
        /// <param name="query">The RSQL query for filtering.</param>
        /// <param name="orderBy">The order with parameters separated by a comma. Use "-" for descending order.</param>
        /// <param name="limit">Sets the maximal number of records to return.</param>
        /// <param name="offset">Sets the offset of the first result. orderBy must be set.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded programmes.</returns>
        public Task<IReadOnlyList<Programme>> GetProgrammes
        (
            string? query = null,
            string orderBy = "id",
            ushort limit = 10,
            int offset = 0,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme by its code.
        /// </summary>
        /// <param name="code">The code of the programme.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded programme.</returns>
        public Task<Programme?> GetProgramme
        (
            string code,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme by loadable entity.
        /// </summary>
        /// <param name="programmeLoadableEntity">The programme loadable entity.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded programme.</returns>
        public Task<Programme?> GetProgramme
        (
            AtomLoadableEntity<Programme> programmeLoadableEntity,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme branches by programme's code.
        /// </summary>
        /// <param name="programmeCode">The code of the programme.</param>
        /// <param name="query">The RSQL query for filtering.</param>
        /// <param name="orderBy">The order with parameters separated by a comma. Use "-" for descending order.</param>
        /// <param name="limit">Sets the maximal number of records to return.</param>
        /// <param name="offset">Sets the offset of the first result. orderBy must be set.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded programme.</returns>
        public Task<IReadOnlyList<Branch>> GetProgrammeBranches
        (
            string programmeCode,
            string? query = null,
            string orderBy = "id",
            ushort limit = 10,
            int offset = 0,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme branches by programme's loadable entity.
        /// </summary>
        /// <param name="programmeLoadableEntity">The programme loadable entity.</param>
        /// <param name="query">The RSQL query for filtering.</param>
        /// <param name="orderBy">The order with parameters separated by a comma. Use "-" for descending order.</param>
        /// <param name="limit">Sets the maximal number of records to return.</param>
        /// <param name="offset">Sets the offset of the first result. orderBy must be set.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded branches.</returns>
        public Task<IReadOnlyList<Branch>> GetProgrammeBranches
        (
            AtomLoadableEntity<Programme> programmeLoadableEntity,
            string? query = null,
            string orderBy = "id",
            ushort limit = 10,
            int offset = 0,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme courses by programme's code.
        /// </summary>
        /// <param name="programmeCode">The code of the programme.</param>
        /// <param name="query">The RSQL query for filtering.</param>
        /// <param name="orderBy">The order with parameters separated by a comma. Use "-" for descending order.</param>
        /// <param name="limit">Sets the maximal number of records to return.</param>
        /// <param name="offset">Sets the offset of the first result. orderBy must be set.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded courses.</returns>
        public Task<IReadOnlyList<Course>> GetProgrammeCourses
        (
            string programmeCode,
            string? query = null,
            string orderBy = "id",
            ushort limit = 10,
            int offset = 0,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme courses by programme's loadable entity.
        /// </summary>
        /// <param name="programmeLoadableEntity">The programme loadable entity.</param>
        /// <param name="query">The RSQL query for filtering.</param>
        /// <param name="orderBy">The order with parameters separated by a comma. Use "-" for descending order.</param>
        /// <param name="limit">Sets the maximal number of records to return.</param>
        /// <param name="offset">Sets the offset of the first result. orderBy must be set.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded courses.</returns>
        public Task<IReadOnlyList<Course>> GetProgrammeCourses
        (
            AtomLoadableEntity<Programme> programmeLoadableEntity,
            string? query = null,
            string orderBy = "id",
            ushort limit = 10,
            int offset = 0,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme courses groups by programme's code.
        /// </summary>
        /// <param name="programmeCode">The code of the programme.</param>
        /// <param name="query">The RSQL query for filtering.</param>
        /// <param name="orderBy">The order with parameters separated by a comma. Use "-" for descending order.</param>
        /// <param name="limit">Sets the maximal number of records to return.</param>
        /// <param name="offset">Sets the offset of the first result. orderBy must be set.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded courses groups.</returns>
        public Task<IReadOnlyList<CoursesGroup>> GetProgrammeCoursesGroups
        (
            string programmeCode,
            string? query = null,
            string orderBy = "id",
            ushort limit = 10,
            int offset = 0,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme courses groups by programme's loadable entity.
        /// </summary>
        /// <param name="programmeLoadableEntity">The programme loadable entity.</param>
        /// <param name="query">The RSQL query for filtering.</param>
        /// <param name="orderBy">The order with parameters separated by a comma. Use "-" for descending order.</param>
        /// <param name="limit">Sets the maximal number of records to return.</param>
        /// <param name="offset">Sets the offset of the first result. orderBy must be set.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded courses groups.</returns>
        public Task<IReadOnlyList<CoursesGroup>> GetProgrammeCoursesGroups
        (
            AtomLoadableEntity<Programme> programmeLoadableEntity,
            string? query = null,
            string orderBy = "id",
            ushort limit = 10,
            int offset = 0,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme study plans by programme's code.
        /// </summary>
        /// <param name="programmeCode">The code of the programme.</param>
        /// <param name="query">The RSQL query for filtering.</param>
        /// <param name="orderBy">The order with parameters separated by a comma. Use "-" for descending order.</param>
        /// <param name="limit">Sets the maximal number of records to return.</param>
        /// <param name="offset">Sets the offset of the first result. orderBy must be set.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded study plans.</returns>
        public Task<IReadOnlyList<StudyPlan>> GetProgrammeStudyPlans
        (
            string programmeCode,
            string? query = null,
            string orderBy = "id",
            ushort limit = 10,
            int offset = 0,
            string? lang = null,
            CancellationToken token = default
        );

        /// <summary>
        /// Gets programme study plans by programme's loadable entity.
        /// </summary>
        /// <param name="programmeLoadableEntity">The programme loadable entity.</param>
        /// <param name="query">The RSQL query for filtering.</param>
        /// <param name="orderBy">The order with parameters separated by a comma. Use "-" for descending order.</param>
        /// <param name="limit">Sets the maximal number of records to return.</param>
        /// <param name="offset">Sets the offset of the first result. orderBy must be set.</param>
        /// <param name="lang">The request language. Supports cs or en.</param>
        /// <param name="token">The cancellation token for the operation.</param>
        /// <returns>The loaded study plans.</returns>
